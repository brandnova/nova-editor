const EmojiPicker = () => {
  const editor  = useSlate()
  const [query, setQuery] = useState("")

  const insert = (emoji, e) => {
    e.preventDefault()
    Transforms.insertText(editor, emoji)
  }

  const filtered = useMemo(() => {
    if (!query.trim()) return EMOJI_DATA
    // Simple search — match emoji by Unicode name isn't practical without a lookup,
    // so we search by category label
    const q = query.toLowerCase()
    return EMOJI_DATA
      .map(cat => ({
        ...cat,
        emojis: cat.label.toLowerCase().includes(q) ? cat.emojis : [],
      }))
      .filter(cat => cat.emojis.length > 0)
  }, [query])

  return (
    <div
      className="ne-emoji-picker"
      onMouseDown={e => e.stopPropagation()}
    >
      {/* Search */}
      <div className="ne-emoji-search-wrap">
        <Search className="ne-emoji-search-icon" />
        <input
          type="text"
          className="ne-emoji-search-input"
          value={query}
          onChange={e => setQuery(e.target.value)}
          placeholder="Search categories…"
          aria-label="Search emoji categories"
        />
      </div>

      {/* Grid */}
      <div className="ne-emoji-scroll">
        {filtered.map(cat => (
          <div key={cat.label} className="ne-emoji-category">
            <span className="ne-emoji-category-label">{cat.label}</span>
            <div className="ne-emoji-grid">
              {cat.emojis.map(emoji => (
                <button
                  key={emoji}
                  type="button"
                  className="ne-emoji-btn"
                  onMouseDown={e => insert(emoji, e)}
                  title={emoji}
                  aria-label={emoji}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        ))}
        {filtered.length === 0 && (
          <p className="ne-emoji-empty">No results for "{query}"</p>
        )}
      </div>
    </div>
  )
}

export default EmojiPicker
